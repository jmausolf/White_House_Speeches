def month(string):
	"""Convert month string to numeric string."""

	#Uppercase
	if string == "January":
		out = "01"
	if string == "February":
		out = "02"
	if string == "March":
		out = "03"
	if string == "April":
		out = "04"
	if string == "May":
		out = "05"
	if string == "June":
		out = "06"
	if string == "July":
		out = "07"
	if string == "August":
		out = "08"
	if string == "September":
		out = "09"
	if string == "October":
		out = "10"
	if string == "November":
		out = "11"
	if string == "December":
		out = "12"
	
	#Lowercase
	if string == "january":
		out = "01"
	if string == "february":
		out = "02"
	if string == "march":
		out = "03"
	if string == "april":
		out = "04"
	if string == "may":
		out = "05"
	if string == "june":
		out = "06"
	if string == "july":
		out = "07"
	if string == "august":
		out = "08"
	if string == "september":
		out = "09"
	if string == "october":
		out = "10"
	if string == "november":
		out = "11"
	if string == "december":
		out = "12"


	return out


